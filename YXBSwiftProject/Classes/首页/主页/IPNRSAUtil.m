// rsa验签-SHA1

+ (BOOL)rsaVerifySignature:(NSString *)signature plainString:(NSString *)plainString WithPublicKey:(NSString *)publickey{
    if (!signature || !plainString || !publickey) {
        NSLog(@"参数不能为空");
        return false;
    }
    
    SecKeyRef key = [IPNRSAUtil addPublicKey:publickey];
    size_t signedHashBytesSize = SecKeyGetBlockSize(key);
    
    // 要验证的签名
    NSData *signatureData = [[NSData alloc] initWithBase64EncodedString:signature options:0];
    const void *signedHashBytes = [signatureData bytes];

    
    // 待验证的字符串
    NSData *plainData = [plainString dataUsingEncoding:NSUTF8StringEncoding];

    size_t hashBytesSize = CC_SHA1_DIGEST_LENGTH;
    uint8_t *hashBytes = malloc(hashBytesSize);
    if (!CC_SHA1([plainData bytes], (CC_LONG)[plainData length], hashBytes)) {
        return false;
    }
    
    OSStatus status = SecKeyRawVerify(key,
                                      kSecPaddingPKCS1SHA1,
                                      hashBytes,      // SHA1签名之后的原字符串
                                      hashBytesSize,  // SHA1签名的长度
                                      signedHashBytes, // 签名值
                                      signedHashBytesSize); // 公钥长度(签名值不能超过公钥的长度)
    
    if(hashBytes) free(hashBytes);
    
    return status == errSecSuccess;
}

// rsa签名-SHA1
+ (NSString *)rsaSignString:(NSString *)plainString WithPrivateKey:(NSString *)privateKey{
    if (!plainString || !privateKey) {
        NSLog(@"参数不能为空");
        return nil;
    }
    
    SecKeyRef key = [IPNRSAUtil addPrivateKey:privateKey];
    size_t signedHashBytesSize = SecKeyGetBlockSize(key);
    
    NSData *plainData = [plainString dataUsingEncoding:NSUTF8StringEncoding];
    
    uint8_t *signedHashBytes = malloc(signedHashBytesSize);
    memset(signedHashBytes, 0x0, signedHashBytesSize);
    
    size_t hashBytesSize = CC_SHA1_DIGEST_LENGTH;
    uint8_t *hashBytes = malloc(hashBytesSize);
    
    if (!CC_SHA1([plainData bytes], (CC_LONG)[plainData length], hashBytes)) {
        return nil;
    }
    
    SecKeyRawSign(key,
                  kSecPaddingPKCS1SHA1,
                  hashBytes,
                  hashBytesSize,
                  signedHashBytes,
                  &signedHashBytesSize);

    NSData *signedHash = [NSData dataWithBytes:signedHashBytes
                                        length:(NSUInteger)signedHashBytesSize];
    
    signedHash = [signedHash base64EncodedDataWithOptions:0];
    NSString *signedString = [[NSString alloc] initWithData:signedHash encoding:NSUTF8StringEncoding];
    
    if (hashBytes) free(hashBytes);
    if (signedHashBytes) free(signedHashBytes);
    
    return signedString;
}

// 将参数进行MD5加密，空值舍弃
+ (NSString *)queryMD5String:(NSDictionary *)dictionary {
    NSString *queryString = [self generateQueryString:dictionary];
    NSString *md5String = [self generateMD5String:queryString];
    return md5String;
}

+ (NSString *)generateMD5String:(NSString *)input {
    const char *cStr = [input UTF8String];
    unsigned char digest[CC_MD5_DIGEST_LENGTH];
    CC_MD5(cStr, (CC_LONG)strlen(cStr), digest);
    NSMutableString *output = [NSMutableString stringWithCapacity:CC_MD5_DIGEST_LENGTH * 2];
    for (int i = 0; i < CC_MD5_DIGEST_LENGTH; i++) {
        [output appendFormat:@"%02x", digest[i]];
    }
    return output;
}

+ (NSString *)generateQueryString:(NSDictionary *)dictionary {
    NSMutableArray *filteredKeyValuePairs = [NSMutableArray array];
    NSArray *sortedKeys = [dictionary.allKeys sortedArrayUsingSelector:@selector(compare:)];
    for (NSString *key in sortedKeys) {
        id value = dictionary[key];
        if (![value isKindOfClass:[NSNull class]] && ![value isEqual:@""]) {
            NSString *keyValuePair = [NSString stringWithFormat:@"%@=%@", key, value];
            [filteredKeyValuePairs addObject:keyValuePair];
        }
    }
    return [filteredKeyValuePairs componentsJoinedByString:@"&"];
}

// 验签以及加密, 空参数不加密
+ (NSDictionary  * _Nullable)signAndSecret:(NSDictionary *)dic {
    if (dic == nil || dic.allKeys.count == 0) {
        return nil;
    }
    
    NSString *sign = [IPNRSAUtil queryMD5String:dic];
    NSMutableDictionary *mutableDic = [NSMutableDictionary dictionaryWithDictionary:dic];
    mutableDic[@"sign"] = sign;
    NSString *encryptString = [IPNRSAUtil encryptString:mutableDic.mj_JSONString publicKey:@"请填入秘钥"];
    // 最终的加密参数
    NSDictionary *parma = @{@"data": encryptString};
    return  parma;
}

